<h2>¿Qué es la reunificación de deudas?</h2>
 
<div class="grid">
 
	<div class="reunificacion">
<p>La reunificación de deudas consiste en <b>agrupar todas las financiaciones que hayamos contraído en una sola</b>.</p>
<p>El objetivo principal de una <b>reunificación es reducir la cantidad que pagamos al mes</b>, a la vez que pasamos a abonar una sola cuota mensual.</p>
 
		<p><u><b>Lo que hace diferente nuestra reunificación:</b></u></p>
<ul>
<li>Pagar una sola cuota mensual</li>
<li>Reducir la cantidad total que deberías pagar al mes</li>
<li>Reducir el total a pagar de cada una de ellas</li>
</ul>
